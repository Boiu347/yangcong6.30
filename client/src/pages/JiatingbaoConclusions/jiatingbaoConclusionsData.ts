export interface VocClip {
  /** 展示的原话文本 */
  text: string;
  /** 用户来源，如「黄妈妈 · 广州 · 3年级&6年级」 */
  source: string;
  /** 音频切片地址；缺省表示暂无现成录音，仅展示文本 */
  clipUrl?: string;
}

export interface DocImage {
  src: string;
  caption?: string;
}

export interface Point {
  /** 要点标签，如 前提 / 核心 / 第一层 / 画像 */
  label?: string;
  /** 要点正文 */
  text: string;
  /** 访谈录音片段 */
  quotes?: VocClip[];
  /** 问卷 / 行业 / 数据等文字论据 */
  notes?: string[];
  /** 配图论据 */
  images?: DocImage[];
}

export interface SubSection {
  /** 副标题；为空则不单独渲染标题 */
  title: string;
  points: Point[];
}

export interface MainConclusion {
  id: string;
  /** 主标题（结论列表短标签取 —— / ： 前的部分） */
  title: string;
  /** 一句话摘要 */
  summary: string;
  /** 关键洞察（详情顶部高亮框） */
  insight: string;
  subs: SubSection[];
  /** 底部来源说明 */
  evidenceNote: string;
}

export interface DimensionSection {
  id: string;
  label: string;
  desc: string;
  iconKey: 'core' | 'motivation' | 'concern' | 'trust' | 'value' | 'playbook';
  color: string;
  mains: MainConclusion[];
}

