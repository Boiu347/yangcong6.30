/** 统计原声条数 */
export function countJtbVocClips(): number {
  let count = 0;
  jiatingbaoConclusionSections.forEach((section) =>
    section.mains.forEach((main) =>
      main.subs.forEach((sub) =>
        sub.points.forEach((point) => {
          count += point.quotes?.length ?? 0;
        }),
      ),
    ),
  );
  return count;
}

/** 统计涉及的用户来源数（按“｜/·”前的称呼去重） */
export function countJtbUsers(): number {
  const users = new Set<string>();
  jiatingbaoConclusionSections.forEach((section) =>
    section.mains.forEach((main) =>
      main.subs.forEach((sub) =>
        sub.points.forEach((point) =>
          point.quotes?.forEach((quote) => {
            const who = quote.source.split(/·|｜/)[0].trim();
            if (who) users.add(who);
          }),
        ),
      ),
    ),
  );
  return users.size;
}
